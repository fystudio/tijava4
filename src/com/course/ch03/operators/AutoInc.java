package com.course.ch03.operators;

import static net.mindview.util.Print.print;
//auto increment and decrement
public class AutoInc {
    public static void main(String[] args){
        int i =1;
        print("i:"+i);
        print("++i:"+ ++i);
        print("i++:"+ i++);
        print("i:"+i);
        print("--i:"+--i);
        print("i--:"+i--);
        print("i:"+i);
    }
}
